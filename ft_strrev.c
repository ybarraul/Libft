#include "libft.h"

void	ft_strrev(char *s)
{
	char *rev;
	char c;

	rev = s + ft_strlen(s) - 1;
	while (s < rev)
	{
		c = *s;
		*s++ = *rev;
		*rev-- = c;
	}
}
